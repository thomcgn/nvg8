);

CREATE INDEX IF NOT EXISTS ix_anlass_catalog_code ON anlass_catalog(code);
CREATE INDEX IF NOT EXISTS ix_anlass_catalog_label ON anlass_catalog USING gin (to_tsvector('german', label));

