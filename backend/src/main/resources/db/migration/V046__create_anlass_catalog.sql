    id               BIGSERIAL PRIMARY KEY,
    code             VARCHAR(120) NOT NULL,
    label            VARCHAR(300) NOT NULL,
    category         VARCHAR(150),
    default_severity SMALLINT,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
